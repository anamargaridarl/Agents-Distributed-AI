public class Coordinates {
    protected Integer x;
    protected Integer y;

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public Coordinates(Integer x, Integer y){this.x = x; this.y = y;}
}
